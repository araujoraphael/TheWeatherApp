import UIKit
extension UIColor {
    static func searchButtonEnabledColor() -> UIColor {
        return UIColor(red: 51/255.0, green: 148/255.0, blue: 20/255.0, alpha: 1.0)
    }
    
    static func searchButtonDisabledColor() -> UIColor {
        return UIColor(red: 169/255.0, green: 0/255.0, blue: 0/255.0, alpha: 1.0)
    }
}